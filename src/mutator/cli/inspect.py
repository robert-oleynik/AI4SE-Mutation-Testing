import pathlib

import click

from ..inspect.app import InspectApp


@click.command(
    help="""
    View the generated mutation with the associated metadata generated by the test step.
    Use this sub command to add annotations to the test result. Requires to run `test`
    before.
    """
)
@click.option(
    "-o",
    "--out-dir",
    type=pathlib.Path,
    default=pathlib.Path("out", "mutations"),
    show_default=True,
    help="Path to directory containing the mutations. Same as for `test`",
)
@click.option(
    "-p",
    "--project",
    type=pathlib.Path,
    default=pathlib.Path("."),
    show_default=True,
    help="Path to project directory",
)
def inspect(out_dir, project):
    app = InspectApp(project, out_dir)
    app.run()
